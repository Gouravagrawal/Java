    private void populateAmazonTable(){
    for (Network country : system.getNetworkList()) { 
        for (Enterprise ent : country.getEnterpriseDirectory().getEnterpriseList()) { 
            for(UserAccount ua : ent.getUserAccountDirectory().getUserAccountList()) {
                DefaultTableModel model= (DefaultTableModel)Jtable.getModel();
            model.setRowCount(0); 
            
            Object row[]=new Object[2];
            row[0]=country.getNetworkname();
            row[1]=ua.getUsername();
            model.addRow(row); 
            } 
        } 
    } 
} 
    
    private void populateCountryCombo(){ 
    networkJComboBox.removeAllItems();
    
    for (Network net : system.getNetworkList()) { 
        networkJComboBox.addItem(net); 
    
    } 
}
