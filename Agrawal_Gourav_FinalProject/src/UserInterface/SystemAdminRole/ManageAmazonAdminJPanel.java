    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

       String username = usernameJTextField.getText();
            String password = String.valueOf(passwordJField.getPassword());
            String empName = nameJTextField.getText();
            
            if(usernameJTextField.getText().trim().isEmpty()||passwordJField.getText().trim().isEmpty() || nameJTextField.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,"INVALID ENTRY");
            }
            
            else{
                         UserAccount ua = system.getUserAccountDirectory().authenticateUser(username, password);
                         if(ua==null)
                         {
                             
                             AmazonEnterprise ae= null;
                for (Network country : system.getNetworkList())
                
                    for (Enterprise ent : country.getEnterpriseDirectory().getEnterpriseList())
                    
                        if(ent instanceof AmazonEnterprise)
                        {
                            ae=(AmazonEnterprise)ent;
                            Employee empl=ae.getEmployeeDirectory().createEmployee(empName);
                        ua= ae.getUserAccountDirectory().authenticateUser(username, password);
                        if(ua==null )
                        {
                            UserAccount acc= ae.getUserAccountDirectory().createUserAccount();
                            acc.setPassword(password);
                            acc.setEmployee(empl);
                            acc.setUsername(username);
                            acc.setRole(new AmazonAdminRole());
                }
                        }
                        populateAmazonTable();         
                           JOptionPane.showMessageDialog(null,"Created Succefully");
                    
                
                         }
        }


    }//GEN-LAST:event_submitJButtonActionPerformed

