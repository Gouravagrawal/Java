    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        int selectedRow =retailerOrderTable.getSelectedRow();
        
        
       if (selectedRow < 0) {
            return;
       }
            OrderWorkRequest request=(OrderWorkRequest)retailerOrderTable.getValueAt(selectedRow,0);
             order =new Order();
            for(OrderItem oi:request.getOrderItemList()){
                
              Product p=oi.getProduct();
              int quant=oi.getQuantity();
              double totalPrice=oi.getProduct().getTargetP();
              
              OrderItem o1=order.addOrderItem();
              o1.setProduct(p);
              o1.setQuantity(quant);
              o1.setSellingP(totalPrice);
            }
            
            request.setOrder(order);
            request.setStatus("Shipment Under Process");
            
            ShipmentOrganization rso=null;
            for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList()){
                if(org instanceof ShipmentOrganization){
                    
                    rso=(ShipmentOrganization)org;
                    rso.getWorkQueue().addWorkRequest(request);
                
            }
            
            }
        
            populateOrderTable();
    }//GEN-LAST:event_jButton1ActionPerformed

