      private void populateOrderTable() {
        DefaultTableModel model = (DefaultTableModel)retailerOrderTable.getModel();
        model.setRowCount(0);

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof OrderWorkRequest) {
                orderWorkR = (OrderWorkRequest)wr;

                Object row[] = new Object[4];
                row[0] = orderWorkR;
                //row[1] = orderWorkR.getProductID();
                row[1] = orderWorkR.getStatus();
                row[2] = orderWorkR.getRequestDate();
                row[3] = orderWorkR.getResolveDate();
                

                model.addRow(row);

            }
        }





    }

